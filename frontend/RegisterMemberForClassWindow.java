    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String memberID = jTextField1.getText();
        String classID = jTextField2.getText();
        String date = (String) jComboBox1.getSelectedItem();
        LocalDate local = null;

        if (date != null) {
            try {
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
                local = LocalDate.parse(date, formatter);

            } catch (DateTimeParseException ex) {
                ex.printStackTrace();
            }
        }
        TrainerRole tr = new TrainerRole();
        if (memberID.equals("") || classID.equals("")) {
            JOptionPane.showMessageDialog(this, "Some fields are empty!", "Message", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
        } else if (tr.registerMemberForClass(memberID, classID, local)) {

            tr.logout();
            JOptionPane.showMessageDialog(this, "The member with ID= " + memberID + " has been successfully registered to class with ID= " + classID, "Message", JOptionPane.PLAIN_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            this.dispose();
            new TrainerRoleWindow().setVisible(true);

        } else {
            if (tr.classDatabase.contains(classID)) {
                if (tr.classDatabase.getRecord(classID).getAvailableSeats() == 0) {
                    JOptionPane.showMessageDialog(this, "The class has no available seats!", "Message", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                    jTextField2.setText("");
                  this.dispose();
                new TrainerRoleWindow().setVisible(true);   
                }
            } else {
                JOptionPane.showMessageDialog(this, "Registration failed !!", "Message", JOptionPane.ERROR_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
               this.dispose();
                new TrainerRoleWindow().setVisible(true);   
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

